<h4 class="text-center" style="page-break-before: always">CONDITIONAL LIQUIDITY SUPPORT GRANT AGREEMENT (CLSGA)</h4>
<p>
    This Conditional Liquidity Support Grant Agreement (CLSGA) is entered into this
    <strong>{{ now()->format('jS') }}</strong> day of
    <strong>{{ now()->format('F Y') }}</strong> by the Water Sector Trust Fund, of P.O Box 49699-00100 Nairobi, Kenya
    (hereinafter referred to as the “The WSTF”) of the one part,
    the County Government of to be <strong>{{ $wsp->postalcode->county }}</strong>, established under the County Governments Act, (Act
    No. 17 of 2012) of
    P.O Box <strong>{{ $wsp->postalcode->postal_address }}</strong>, in the Republic of Kenya (hereinafter referred to
    as the “County Government (CG)”) of the other part,
    and the to be <strong>{{$wsp->name }}</strong>, established under the Companies
    Act, Chapter 486 Laws of Kenya of Post Office Box to be inserted in the Republic of Kenya (hereinafter referred to
    as the “Company” or “WSP”) of the last part.
</p>
<h3 class="text-center">Article-1</h3>
<h4 class="text-center">Scope of Agreement</h4>
<p>1.1 The Conditional Liquidity Support Grant (CLSG) Agreement describes:</p>
<ol style="list-style-type: lower-alpha">
    <li>The roles and responsibilities of WSTF, CG and WSP.</li>
    <li>The procedures for CLSG calculation and disbursement.</li>
    <li>The role of the Water Services Regulatory Board (WASREB) in verification of the performance of the WSP.</li>
    <li>The required reporting arrangements by the WSP under the CLSG scheme.</li>
</ol>
<br>
<h3 class="text-center">Article-2</h3>
<h4 class="text-center">Conditions Precedent</h4>
<p>
    2.01	Commencement and Termination
</p>
<p>This Agreement shall be deemed to have come into force on the date of signature subject to the following conditions precedent:</p>
<p>The aforementioned amount is provided to be spent in the following categories up to the amounts indicated
    below:</p>
2.01.1	Signed Financing Agreement between the International Development Association (hereinafter referred to as “Association”) and the National Treasury (hereinafter referred to as “Recipient”)<br>
2.01.2	Signed Subsidiary Agreement between the Recipient and WSTF.<br>
2.01.3	Signed Memorandum of Understanding between WSTF and WASREB.<br>
2.01.4	WSP has submitted required documents complying with the eligibility criteria for participating in the CLSG scheme.<br>
<br>
<h3 class="text-center">Article-3</h3>
<h4 class="text-center">The Conditional Liquidity Support Grant</h4>
<p>3.01 	WSTF agrees to provide to the WSP and WSP agrees to receive, the Conditional Liquidity Support Grant upon terms and conditions set forth herein, an amount of Kenya Shillings to be inserted (Kshs. to be inserted) (hereinafter called “the Conditional Liquidity Support Grant”).
</p>
<p>3.02 	The Conditional Liquidity Support Grant shall be used exclusively for the implementation of the Project, as defined in the Subsidiary Grant Agreement between GOK and WSTF, the Conditional Liquidity Support Grant Manual and this Conditional Liquidity Support Grant Agreement.
</p>
<p>3.03 	WST’s obligation to transfer the Conditional Liquidity Support Grant to the WSP shall be contingent upon Government of Kenya making the Financing available to WSTF for the purposes of the Project.
</p>
<p>3.04 	The WSP shall use the proceeds of the Conditional Liquidity Support Grant to finance O&M costs necessary to keep water flowing (e.g. chemical costs, electricity/energy costs, staff salaries, regulatory levies and other fees, and spare parts for essential maintenance), as well as short-term COVID-19 emergency response interventions that can be executed within three months as described in the Conditional Liquidity Grant Support Manual.
</p>
<p>3.05 	The Conditional Liquidity Grant facility will run from 1st May, 2020 to 31st December, 2020.
</p>
<br>
<h3 class="text-center" style="page-break-before: always">Article-4</h3>
<h4 class="text-center">Obligations of the WSTF</h4>
<p>4.01	The WSTF shall:</p>
<p>4.01.1	Provide CLSG Transfers to eligible WSPs to finance Operation and Maintenance Costs for service delivery, and to support COVID-19 emergency response interventions, in accordance with the eligibility criteria and procedures set forth in the CLSG manual.
</p>
<p>4.01.2	Conduct an appraisal in accordance with guidelines set forth in the CLSG manual, to determine and establish, that any proposed CLSG sub-project (A) is consistent with the objectives of the CLSG scheme as set forth in the Financing Agreement between the Association and Recipient; and (B) if, safeguard management tools and instruments for the CLSG sub-project are required, such management tools and instruments have been prepared and approved by the Association in accordance with the provisions of the Financing Agreement and all measures required to be carried out prior to commencement of the activities covered by said instruments have been carried out.
</p>
<p>4.01.3	Review COVID-19 Business Continuity Plans (BCPs) and Emergency Response Plans (ERPs) and baseline data provided by WSPs.
</p>
<p>4.01.4	In collaboration with WASREB, advise WSPs on priorities for COVID-19 emergency response interventions in informal and low-income settlements.
</p>
<p>4.01.5	Monitor compliance with the fiduciary and safeguards arrangements for CLSG Transfers as set forth in the CLSG manual.</p>
<p>4.01.6	In collaboration with WASREB, inform WSPs on the methodology for determining the amount of CLSG Transfers.</p>
<p>4.01.7	Enter a Memorandum of Understanding with WASREB to perform the independent verification of performance of WSPs among other roles as outlined in the MOU.
</p>
<p>4.01.8	Ensure that CLSG Transfers are not availed to a WSP unless and until WSTF has received from WASREB a verification report, showing that such WSP has achieved the performance indicators corresponding to such CLSG Transfer as specified in the CLSG Manual.
</p>
<p>4.01.9	Provide accompanying technical assistance associated with management of the CLSG.</p>
<p>4.01.10	Suspend or terminate the right of any WSP to use the proceeds of the CLSG Transfer, or obtain a refund of all or any part of the amount of the CLSG Transfer, upon the WSP’s failure to perform any of its obligations under the CLSG Agreement.
</p>
<p>4.01.11	Conduct audit of the CLSG at the end of the grant period.</p>
<h3 class="text-center">Article-5</h3>
<h4 class="text-center">Obligations of the WSP</h4>
<p>5.01	The WSP shall:</p>
<p>
    5.01.1	Prepare a Business Continuity Plan (BCP) detailing how the WSP will prioritize their activities to ensure continuity and quality of service during the COVID-19 pandemic period Define and agree priority areas for service improvement in consultation with WASREB.
</p>
<p>5.01.2	Define priority areas for short-term COVID-19 emergency response interventions in informal and low-income settlements, in consultation with WSTF, such as provision of handwashing stations, water tanker services, installing house connections and public waterpoints.
</p>
<p>5.01.3	Prepare a COVID-19 Emergency Response Plan (ERP) outlining the short-term, low risk emergency COVID interventions needed to improve services in informal and low-income settlements.
</p>
<p>5.01.4	Utilize the CLSG Transfers to finance Operation and Maintenance Costs for service delivery, and to support COVID-19 emergency response interventions.
</p>
<p>5.01.5	Implement COVID-19 emergency interventions in accordance with the provisions of the Environmental and Social Management Framework, and all plans prepared thereunder, and any additional social and environmental safeguard measures described in the CLSG manual.
</p>
<p>
    5.01.6	Utilize CLSG Transfers with due diligence and efficiency and in accordance with sound technical, economic, financial, managerial, environmental and social standards and practices, including in accordance with the provisions of the Anti-Corruption Guidelines.
</p>
<p>
    5.01.7	Procure the goods, works and services to be financed out of the CLSG in accordance with applicable World Bank Procurement Guidelines.
</p>
<p>
    5.01.8	Maintain policies and procedures adequate to enable WSTF to monitor and evaluate the progress and performance of the CLSG
</p>
<p>
    5.09.9	Maintain a financial management system and prepare financial statements in accordance with consistently applied accounting standards, both in a manner adequate to reflect the operations, resources and expenditures related to the CLSG; and at the Association’s, WSTF’s or the Recipient’s request, have such financial statements audited by independent auditors, in accordance with consistently applied auditing standards, and promptly furnish the statements as so audited to WSTF, the Recipient and the Association;
</p>
<p>
    5.01.10	Prepare and submit in a timely manner, monthly and quarterly progress reports, and any other requisite report to WSTF on BCP and ERP activities in a format provided in the CLSG manual.
</p>
<p>5.01.11	Enable WSTF, the Recipient, and the Association to inspect the CLSG Sub-projects, its operation and any relevant records and documents; and prepare and furnish to WSTF, the Recipient, and the Association all such information as shall reasonably request relating to the foregoing.
</p>
<p>5.01.12	Prepare and submit a monthly CLSG performance scorecard to WASREB in accordance with the procedures set forth in the CLSG manual.
</p>
<p>5.01.13	Provide, in a timely manner, any documents and accurate information requested by WASREB for purposes of independent verification of performance.
</p>
<p>5.01.14	Request WSTF to initiate CLSG Transfer upon acceptable Performance Verification Report from the WASREB.</p>
<p>5.01.15	Follow up on any issues raised by WASREB in the Performance Verification Report.</p>
<p>5.01.16	Prepare post-COVID financial turnaround/recovery plan and submit to WSTF and WASREB.</p>
<p>5.01.17	Attend and participate in the quarterly meetings and other meetings called by WSTF and WASREB.</p>
<p>5.01.18	Retain documentary evidence and miscellaneous documents for a period of ten (10) years following the last drawdown made under the project (contracts, orders, invoices, bills, receipts and other documents) evidencing expenditure; and
</p>
<p>5.01.19	Allow the representatives of WSTF and the Government examine such records.</p>

<h3 class="text-center">Article-6</h3>
<h4 class="text-center">Obligations of the County Government</h4>
<p>6.01	The County Government undertakes to:</p>
<p>
    6.01.1	Support the satisfactory implementation and conclusion of all activities funded by WSTF under the Conditional Liquidity Support Grant.
</p>
<p>6.01.2	Undertake random monitoring visit in addition to the regular monitoring of the project.</p>
<p>6.01.3	Support the WSTF in undertaking both its internal and external audit services of the project.</p>
<p>6.01.4	Co-operate closely with the WSTF in making decisions on interventions deemed necessary.</p>
<p>6.01.5	In coordination with the WSTF engage in the end of the project evaluation.</p>
<p>6.01.6	Ensure that the WSP fulfil its obligations as specified in this agreement.</p>
<p>6.01.7	Support and facilitate external auditors that may be engaged in providing auditing services. </p>
<p>6.01.8	Facilitate the availing of land for sites identified for the project where necessary, and compliance with statutory requirements. </p>

<h3 class="text-center">Article-7</h3>
<h4 class="text-center">Specific Conditions</h4>
<p>7.01	CLSG Disbursement: -</p>
<p>7.01.1	The WSP shall be eligible to receive a fixed grant during the first three months after execution of this Agreement.</p>
<p>7.01.2	The amount of the fixed grant shall be equal to the estimated cost of the short-term COVID-19 emergency interventions proposed by the WSP and appraised by WSTF.</p>
<p>7.01.3	The WSP shall be eligible to receive a variable grant throughout the grant period based on pre-agreed multiples of revenue collected by the WSP as further elaborated in Annex 3 to this Agreement.</p>
<p>7.01.4	The WSP shall only be eligible to earn up to a maximum variable grant amount in any given year regardless of the amount of revenue collected.</p>
<p>7.01.5	The variable grant shall be subject to a performance adjustment, based on achievement of the CLSG performance scorecard as further elaborated in Annex 2 and 3 to this Agreement.</p>
<p>7.01.6	The WSP shall not receive a CLSG Transfer unless and until WSTF has received a Performance Verification Report from WASREB, showing that such WSP has achieved the performance indicators corresponding to such CLSG Transfer.</p>

<h3 class="text-center">Article-8</h3>
<h4 class="text-center">Settlement of Disputes and Applicable Law</h4>
<p>8.01	WSTF and the Company shall take all reasonable measures to settle amicably any dispute arising out of the interpretation or application of this Conditional Liquidity Support Grant Agreement.</p>
<p>8.02	If, after thirty (30) days from the commencement of such negotiations the parties have been unable to resolve amicably a dispute, the aggrieved party shall at the first instance, formally declare a dispute by referring the matter to the Summit, the Council or any other intergovernmental structure established under the Intergovernmental Relations Act, 2012, as may be appropriate. </p>
<p>8.03	If the declared dispute is not resolved by the relevant entity established under the Intergovernmental Relations Act, 2012 after the expiry of one hundred and eighty (180) days, a party to the dispute may submit the dispute to arbitration in accordance with the Nairobi Centre for International Arbitration Rules, 2015. </p>
<p>8.04	This Conditional Liquidity Support Grant Agreement shall be governed by and construed in all respect in accordance with the Financing Agreement and the Laws of Kenya.  </p>

<h3 class="text-center">Article-9</h3>
<h4 class="text-center">Intellectual Property Rights</h4>
<p>9.01	WSTF and WSP agree that all rights, title and interest in or to any information, data, reports, documents, procedures, forecasts, technology, Know-How and any other Intellectual Property Rights whatsoever owned by either WSTF or WSP before the Commencement Date or developed by either party during the Project Fund Period, shall remain the property of that party.</p>
<p>9.02	Where WSTF has provided WSP with any of its Intellectual Property Rights for use in connection with the Project (including without limitation its name and logo), WSP shall, on termination of this Agreement, cease to use such Intellectual Property Rights immediately and shall either return or destroy such Intellectual Property Rights as requested by WSTF.
</p>

<h3 class="text-center" style="page-break-before: always">Article-10</h3>
<h4 class="text-center">Confidentiality</h4>
<p>10.01	Each party shall during the term of this Agreement and thereafter keep secret and confidential all Intellectual Property Rights or Know-How or other business or technical information disclosed to it as a result of the Agreement and shall not disclose the same to any person save to the extent necessary to perform its obligations in accordance with the terms of this Agreement or save as expressly authorized in writing by the other party.</p>
<p>10.02	The obligation of confidentiality contained in this clause shall not apply or shall cease to apply to any Intellectual Property Rights, Know-How or other business, technical or commercial information which:</p>
<p>10.02.1	at the time of its disclosure by the disclosing party is already in the public domain or which subsequently enters the public domain other than by breach of the terms of this Agreement by the receiving party;
</p>
<p>10.02.2 is already known to the receiving party as evidenced by written records at the time of its disclosure by the disclosing party and was not otherwise acquired by the receiving party from the disclosing party under any obligations of confidence; or
</p>
<p>10.02.3	is at any time after the date of this Agreement acquired by the receiving party from a third party having the right to disclose the same to the receiving party without breach of the obligations owed by that party to the disclosing party.</p>

