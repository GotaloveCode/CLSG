    11.02	 Any notice, request, etc. to be exchanged between the WSTF and the WSP under this Agreement shall be made in writing. Such notice and request shall be deemed to have been given or made to the party required or said to have been made or given if it is given or delivered at the following address or at any other address specified by a prior notice.
